    public void FechaActual() { //Metodo para mostara la fecha 

        LocalDateTime ahora = LocalDateTime.now();
        int año = ahora.getYear();
        int mes = ahora.getMonthValue();
        int dia = ahora.getDayOfMonth();

        JFecha.setText(dia + "/" + mes + "/" + año);
    }

    public void run() { //Metodo con hilos para actualizar la hora
        DateTimeFormatter formateador = DateTimeFormatter.ofPattern("HH:mm:ss");
        Runnable runnable = () -> {
            while (true) {
                try {
                    Thread.sleep(500);
                    JHora.setText(formateador.format(LocalDateTime.now()));
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        };
        Thread hilo = new Thread(runnable);
        hilo.start();

    }


