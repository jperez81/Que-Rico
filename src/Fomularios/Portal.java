    private void BtnProduccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnProduccionActionPerformed
        if (BtnProduccion.isSelected()) {

            pnl_vista_principal.add(Produccion, "Produccion");
            vistac.show(pnl_vista_principal, "Produccion");
            SwingUtilities.updateComponentTreeUI(this);
            this.repaint();
            //ACTIVAMOS EL BOTON QUE PRESIONAMOS Y BLOQUEAMOS LOS DEMAS
            BtnPañol.setEnabled(false);

//            BtnReportes.setEnabled(false);
            BtnMantenimiento.setEnabled(false);

            BtnProduccion.setBackground(Color.WHITE);

        } else {
            //LIMPIAMOS LAS CAJAS DE LO CONTRARIO
            pnl_vista_principal.removeAll();
            SwingUtilities.updateComponentTreeUI(this);
            this.repaint();
            //Y ACTIVAMOS NUEVAMENTES LOS BOTONES Y DEVOLVEMOS EL COLOR AL BOTON 
            BtnPañol.setEnabled(true);
//            BtnReportes.setEnabled(true);

            BtnMantenimiento.setEnabled(true);
            BtnProduccion.setBackground(new Color(204, 204, 204));

        }
    }//GEN-LAST:event_BtnProduccionActionPerformed

